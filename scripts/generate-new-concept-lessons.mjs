import { readdir, readFile, stat, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { z } from "zod";

const transcriptSchema = z.object({
  version: z.literal(1),
  course: z.object({
    id: z.string().min(1),
    title: z.string().min(1),
    audioFilename: z.string().min(1),
    duration: z.number().positive(),
    language: z.string().min(1).optional()
  }),
  segments: z.array(z.object({
    id: z.string().min(1),
    start: z.number().nonnegative(),
    end: z.number().positive(),
    text: z.string().min(1),
    translations: z.record(z.string(), z.string().min(1)).optional(),
    speaker: z.string().min(1).optional()
  })).min(1)
});

const projectDirectory = resolve(import.meta.dirname, "..");
const collections = [
  { id: "nce1", title: "新概念英语第一册", folder: "新概念1-美音" },
  { id: "nce2", title: "新概念英语第二册", folder: "新概念2-美音" },
  { id: "nce3", title: "新概念英语第三册", folder: "新概念3-美音" },
  { id: "nce4", title: "新概念英语第四册", folder: "新概念4-美音" }
];
const generatedLessons = [];

for (const collection of collections) {
  const directory = resolve(projectDirectory, "public", "新概念", collection.folder);
  const jsonFiles = (await readdir(directory)).filter((name) => name.endsWith(".json")).sort();
  for (const jsonFilename of jsonFiles) {
    const input = transcriptSchema.parse(JSON.parse(await readFile(resolve(directory, jsonFilename), "utf8")));
    if (input.course.language !== "en") {
      throw new Error(`Course language must be en in ${jsonFilename}`);
    }
    const segmentIds = new Set();
    for (let index = 0; index < input.segments.length; index += 1) {
      const segment = input.segments[index];
      if (segmentIds.has(segment.id)) throw new Error(`Duplicate segment ID in ${jsonFilename}: ${segment.id}`);
      segmentIds.add(segment.id);
      if (segment.end <= segment.start) throw new Error(`Invalid segment time in ${jsonFilename}: ${segment.id}`);
      if (index > 0 && segment.start < input.segments[index - 1].end) {
        throw new Error(`Overlapping segments in ${jsonFilename}: ${segment.id}`);
      }
      if (segment.end > input.course.duration + 1) throw new Error(`Segment past duration in ${jsonFilename}: ${segment.id}`);
      if (!segment.translations?.["zh-Hans"]?.trim()) {
        throw new Error(`Missing zh-Hans translation in ${jsonFilename}: ${segment.id}`);
      }
    }
    const audioPath = resolve(directory, input.course.audioFilename);
    const audioStat = await stat(audioPath);
    const baseUrl = `/新概念/${collection.folder}`;
    generatedLessons.push({
      id: input.course.id,
      collection: collection.id,
      title: input.course.title,
      durationLabel: `${Math.max(1, Math.round(input.course.duration / 60))} 分钟`,
      sizeLabel: `${(audioStat.size / 1024 / 1024).toFixed(1)} MB`,
      audioUrl: `${baseUrl}/${input.course.audioFilename}`,
      transcriptUrl: `${baseUrl}/${jsonFilename}`
    });
  }
}

